/* This program calculate the product of 3 number,
 the sum of 5 numbers and a division of 2 numbers
*/
// Product of 3 numbers
// Declare a function to calculate the product of 3 numbers
const productOfThreeNumbers = (a,b,c) => {
    console.log(`Product Of 3(2x2x2) numbers = ${a * b * c}`);
}
productOfThreeNumbers(2,2,2);

// Sum of 5 numbers
// Declare a function to calaculate the sum of 5 numbers
const sumOfFiveNumbers = (v,w,x,y,z) => {
    console.log(`The sum of Five(1+2+3+4+5) numbers = ${v + w + x + y + z}`)
}
sumOfFiveNumbers(1,2,3,4,5)

// Division of 2 numbers
// Declare a function to calculate the division of 2 numbers
const divisionOfTwoNumbers = (r,s) => {
    console.log(`The division of Two(10/2) numbers = ${r/s}`)
}
divisionOfTwoNumbers(10,2)

